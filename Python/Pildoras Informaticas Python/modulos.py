# Que son los modulos?

# Un archivo con extension py o pyc (python copilado) o archivo escrito en c para CPython, que posee su propio espacio de nombres y que puede contener variables, funciones cases e incluso otros modulos.

# Sirven para organizar y reutilizar el codigo. (modularizacion  reutilizacion) -> ejemplos supongamos que tenia codigo de una aplicacion que habia creado anteriormente y ahora lo quiero re utilizar, eso es lo que haria el modulo permitir que podamos reutilizarlo nuevamente.

# modularacion es crear divisiones de partes para tener una mayor organizacion. 

#Ejemplo parte 1

def sumar(op1, op2):
    print("El resultado de la suma es: ", op1 + op2)


def resta(op1, op2):
    print("El resultado de la resta es: ", op1 - op2)

def multiplicacion(op1, op2):
    print("El resultado de la multiplicacion es: ", op1 * op2)


